            <h3>Naprostý začátek</h3>

            <p>Pro naprostý začátek bude nejlepší když budeš chodit cvičit jednou až dvakrát týdně. Tělo pravděpodobně ještě není zvyklé na takový styl pohybu. Dej mu čas se adaptovat. Zároveň ti to pomůže pomalu proniknout do tohoto koníčku. V neposlední řadě ti tento přístup pomůže se nepřehltit a neznechutit si to už od začáítku.</p>

            <p>Při této četnosti je nejlepší volbou odcvičit vždy celé tělo. Do toho patří ruce (biceps a triceps), ramena (přední, zadní a boční), záda, hrudník, břicho (spodní, vrchní a boční) a samozřejmě i nohy (quadricepsy, hamstringy a lýtka).</p>

            <p>Obecně se doporučuje každou partii odcvičit dvakrát týdně. Pokud začínáš pouze s jedním treninkem, tak se ale žádné drama neděje. Zároveň je dobré myslet na odpočinek. U treninku celého těla by nebylo dobré jít dva dny za sebou. Platí tedy mít alespoň jeden až dva dny odpočinku mezi treninky.</p>

            <p>Abys ve fitku nestrávil celý den, tak si dej například dva až tři cviky na partii. Je dobré myslet na to že se teprve učíš. Takže spíše vol přístup více opakování s nižší vahou.</p>

            <h3>Mírně pokročilý trenink</h3>

            <p>Už nějakou dobu cvičíš a chceš se posunout? Nebo jsi jen uvážil, že nechceš začít úplně od začátku? Pak si tu rozebereme trochu pokročilejší přístup. Abych se úplně neopakoval i zde platí stejný přístup k zahřátí a protežení.</p>

            <p>Dalším logickým krokem je cvičit třikrát až čtyřikrát do týdne. Pro toto rozložení nejlépe sedí chodit cvičit obden. Pokud ti to časově nevychátí, tak opět není problém. Stačí myslet na jeden až dva dny odpočinku mezi opakovaným cvičením stejné partie.</p>

            <p>Zde už budeme partie lehce rozdělovat. Při tomto rozložení bude jednou z nejlepších možností si treninky rozdělit na vršek (ramena, ruce, hrudník, záda) a spodek (přední stehna, zadní stehna a lýtka). Břicho si zde můžeš rozdělit dle potřeby, či časové náročnosti.</p>

            <p>Už si můžeš i dovolit více cviků na partii. Na příklad jednou můžeš začít hrudníkem a dát důraz na něho. Příště začít zády a dát důraz na ně.</p>

            <h3>Pokročilý trenink</h3>

            <p>Tento přístup už opravdu nebude vhodný pro naprostý začátek. Mimo to je i časově náročný.</p>

            <p>Sem se nejčastějí řadí dva styly treninku. Jsou tak zvaně push-pull-legs a Arnold split. Případně je možná kombinace. Není však vyloučen už naprosto osobní přístup podle potřeby.</p>

            <p>Tato rozdělení nám už dávají opravdu velkou svobodu v množství cviků na partii. Je však dobrá mezi tyto treninky rozdělit důraz na jednotlivé části hlavních partií. Na příklad u hrudníku jednou důraz na spodní  a střední část a podruhé na vrchní část.</p>

            <h4>Push-pull-legs</h4>

            <p>Zde platí rozdělení partií podle typu pohybu. Tedy tlačivý, tahací a nohy. Rozděleníí partií je následovné do push patří hrudník, tricepsy a ramena. Pull zahrnuje záda a bicepsy. Legs jak už z názvu vyplívá zahrnuje nohy. Vychází to tedy na tři treninky. Břicho zde opět můžeme rozdělit dle potřeby.</p>

            <p>Tyto tři treninky jsou vždy tři v řadě jdoucí dny. Následně můžeme dát den odpočinku a začít znovu.</p>

            <h4>Arnold split</h4>

            <p>Zde už dochází k velkému promíchání. Opět je trenink rozdělen do tří po sobě jdoucích dní i přístup k odpočinku je stejný. První den jsou velké partie vršku, což jsou záda a hrudník. Následují menší partie vršku, ramena a ruce. Zakončení jsou opět nohy. Břicho opět volitelně dle potřeby.</p>

            <p>Pokud bys zvolil tento přístup je opravdu nutné mezi jednotlivými cykly měnit důraz. Na příklad záda a hrudník jsou oboje silově náročné partie. Je tedy dobré střídat jejich pořadí mezi cykly.</p>

